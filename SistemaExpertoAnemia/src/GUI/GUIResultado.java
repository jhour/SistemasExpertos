    private void cargarListaSintomas(ArrayList<String> lista){
        String matriz[][] = new String[lista.size()][2];
        for (int i = 0; i < lista.size(); i++) {
            for(int j = 0; j < 2; j++){
                matriz[i][0] = String.valueOf(i+1);
                matriz[i][1] = lista.get(i).toString();
            }
        }
        
        tabla.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "NÂº", "Sintoma"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.Integer.class, java.lang.String.class
            };
            boolean[] canEdit = new boolean [] {
                false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
        jScrollPane1.setViewportView(tabla);
        if (tabla.getColumnModel().getColumnCount() > 0) {
            tabla.getColumnModel().getColumn(0).setResizable(false);
            tabla.getColumnModel().getColumn(0).setPreferredWidth(55);
            tabla.getColumnModel().getColumn(1).setResizable(false);
            tabla.getColumnModel().getColumn(1).setPreferredWidth(320);
        }
    }
    

