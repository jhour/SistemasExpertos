    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        // TODO add your handling code here:
        nombre = this.txtName.getText();
        int indexPoblacion = txtPoblacion.getSelectedIndex();
        poblacion = "";
        
        switch(indexPoblacion){
            case 0:
                poblacion = "primera_infancia";
                break;
            case 1:
                poblacion = "infancia";
                break;
            case 2:
                poblacion = "pubertad";
                break;
            case 3:
                poblacion = "adolescencia";
                break;
            case 4:
                poblacion = "mujer_gestante";
                break;
            default:
                poblacion = "mujer_lactante";
                break;
        }
        
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                
                new GUISINTOMAS(nombre, poblacion).setVisible(true);
                
            }
        });
        this.setVisible(false);
    }//GEN-LAST:event_btnContinuarActionPerformed

